    private void alterarPerguntaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarPerguntaButtonActionPerformed
        String pergunta = perguntaTextArea.getText();
        String respostaCorreta = respostaCorretaTextArea.getText();
        String respostaIncorreta1 = respostaIncorreta1TextArea.getText();
        String respostaIncorreta2 = respostaIncorreta2TextArea.getText();
        String respostaIncorreta3 = respostaIncorreta3TextArea.getText();
        
        if(pergunta.isBlank() || respostaCorreta.isBlank() || respostaIncorreta1.isBlank() || respostaIncorreta2.isBlank() || respostaIncorreta3.isBlank()){
            JOptionPane.showMessageDialog(null, "Ã preciso preencher todos os campos!", "Quiz ArtVantGarde", 0);
        }else{
            Questao questao = new Questao(codQuestao, pergunta, urlImagemTextArea.getText(), 
                    respostaCorreta, respostaIncorreta1, respostaIncorreta2, respostaIncorreta3, idVanguarda);
            try{
                if(dao.existeQuestao(questao)){
                    try {
                        dao.alterarQuestao(questao);
                        questoesTable.setModel(new QuestaoTableModel(idVanguarda));
                        perguntaTextArea.setText("");
                        urlImagemTextArea.setText("");
                        respostaCorretaTextArea.setText("");
                        respostaIncorreta1TextArea.setText("");
                        respostaIncorreta2TextArea.setText("");
                        respostaIncorreta3TextArea.setText("");
                    }catch (Exception e){
                        JOptionPane.showMessageDialog(null, "Ã preciso selecionar uma questao!");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "A questÃ£o que vocÃª esta tentando alterar nÃ£o existe ou nÃ£o pÃ´de ser encontrada!");
                }
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Problemas tÃ©cnicos!!");
            }
        }
    }//GEN-LAST:event_alterarPerguntaButtonActionPerformed

