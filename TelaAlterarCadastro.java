    public TelaAlterarCadastro(int idLogin, Musica musica) {
        super("Quiz ArtVantGarde");
        initComponents();
        this.setLocationRelativeTo(null);
        this.musica = musica;
        this.idLogin = idLogin;
        
        try{
            if(dao.existeProfessor(idLogin)){
                cadastroAlunoPanel.setVisible(false);
                alunoButtonPanel.setVisible(false);
                esconderSenhaProfessorLabel.setVisible(false);
                cadastroProfessor = dao.pegarCadastrosProfessores(this.idLogin);
                emailProfessorTextField.setText(cadastroProfessor.get(0));
                senhaProfessorPasswordField.setText(cadastroProfessor.get(1));
                nomeProfessorTextField.setText(cadastroProfessor.get(2));
                materiaLecionadaTextField.setText(cadastroProfessor.get(3));
            }else if(dao.existeAluno(idLogin)){
                cadastroProfessorPanel.setVisible(false);
                professorButtonPanel.setVisible(false);
                esconderSenhaAlunoLabel.setVisible(false);
                cadastroAluno = dao.pegarCadastrosAlunos(this.idLogin);
                emailAlunoTextField.setText(cadastroAluno.get(0));
                senhaAlunoPasswordField.setText(cadastroAluno.get(1));
                nomeAlunoTextField.setText(cadastroAluno.get(2));
                serieTextField.setText(cadastroAluno.get(3));
                codMatriculaTextField.setText(cadastroAluno.get(4));
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Problemas técnicos!!");
        }
    }

