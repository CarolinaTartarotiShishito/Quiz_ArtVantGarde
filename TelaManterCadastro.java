    private void adicionarCadastroButtonActionPerformed(java.awt.event.ActionEvent evt) {                                                        
        String email = emailTextField.getText();
        String senha = senhaTextField.getText();
        String nomeCompleto = nomeCompletoTextField.getText();
        String serie = serieTextField.getText();
        String codMatricula = codMatriculaTextField.getText();
        
        UsuarioAluno aluno = new UsuarioAluno(nomeCompleto, codMatricula);
        Login login = new Login(email, senha);
        if(email.isBlank() || senha.isBlank() || nomeCompleto.isBlank() || serie.isBlank() || codMatricula.isBlank()){
            JOptionPane.showMessageDialog(null, "É preciso preencher todos os campos!", "Quiz ArtVantGarde", 0);
        }else{
            try{
                if(dao.existeLogin(email) || dao.existeAluno(aluno)){
                    JOptionPane.showMessageDialog(null, "O email ou aluno inserido(s) já está(ão) cadastrado(s)!", "Quiz ArtVantGarde", 0);
                }else{
                    dao.novoLogin(login);
                    int idLoginAlunoNovo = dao.pegarId(login);
                    aluno = new UsuarioAluno(nomeCompleto, serie, codMatricula, idLoginAlunoNovo);
                    dao.cadastroAluno(aluno);
                    alunosTable.setModel(new CadastroAlunoTableModel());
                    emailTextField.setText("");
                    senhaTextField.setText("");
                    nomeCompletoTextField.setText("");
                    serieTextField.setText("");
                    codMatriculaTextField.setText("");
                    JOptionPane.showMessageDialog(null, "Cadastro criado com sucesso!", "Quiz ArtVantGarde", 1);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Problemas Técnicos");
            }
        }
    }                                                       

