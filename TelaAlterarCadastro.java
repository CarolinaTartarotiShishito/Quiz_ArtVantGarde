    private void alterarAlunoButtonActionPerformed(java.awt.event.ActionEvent evt) {                                                   
        String email = emailAlunoTextField.getText();
        String senha = new String(senhaAlunoPasswordField.getPassword());
        String nomeCompleto = nomeAlunoTextField.getText();
        String serie = serieTextField.getText();
        String codMatricula = codMatriculaTextField.getText();
        if(email.isBlank() || senha.isBlank() || nomeCompleto.isBlank() || serie.isBlank() || codMatricula.isBlank()){
            JOptionPane.showMessageDialog(null, "É preciso preencher todos os campos!", "Quiz ArtVantGarde", 0);
        }else{        
            Login login = new Login(idLogin, email, senha);
            UsuarioAluno aluno = new UsuarioAluno(nomeCompleto, email, codMatricula, idLogin);
            try{
                dao.alterarCadastro(login);
                dao.alterarAluno(aluno);
                JOptionPane.showMessageDialog(null, "Conta alterada com sucesso! Retornando à tela de Login!", "Quiz ArtVantGarde", 1);
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Problemas Técnicos!!", "Quiz ArtVantGarde", 1);
            }
        }
    }                                                  

